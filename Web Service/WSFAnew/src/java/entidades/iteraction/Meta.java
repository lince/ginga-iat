package entidades.iteraction;

public class Meta {

    private int idMeta;
    private String meta;

    /**
     * @return the idMeta
     */
    public int getIdMeta() {
        return idMeta;
    }

    /**
     * @param idMeta the idMeta to set
     */
    public void setIdMeta(int idMeta) {
        this.idMeta = idMeta;
    }

    /**
     * @return the meta
     */
    public String getMeta() {
        return meta;
    }

    /**
     * @param meta the meta to set
     */
    public void setMeta(String meta) {
        this.meta = meta;
    }
}
